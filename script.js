function fourSquared() {

  // 1. Declare a variable called num and set it equal to 4.
  let num = 4;

  // 2. Declare a variable called numSquared and set it equal to num multiplied by itself.
  let numSquared = num * num;

  // 3. Return the variable numSquared
  return numSquared;
}

// 4. Declare a variable called answer and set it equal to a function call of fourSquared.
let answer = fourSquared();

// 5. Console.log the answer variable. You should see the number 16 in the console.
console.log(answer);
